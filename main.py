import os
import subprocess
import requests
import json
from google.oauth2.credentials import Credentials
from googleapiclient.discovery import build
from googleapiclient.http import MediaFileUpload

# === CONFIG ===
BARK_AUDIO_URL = os.getenv("BARK_AUDIO_URL")
BACKGROUND_IMAGE = "background.jpg"

print("ðŸ”Š Downloading Bark audio...")

response = requests.get(BARK_AUDIO_URL)
print(f"Status code: {response.status_code}")
if response.status_code != 200:
    raise Exception(f"Failed to download audio! HTTP {response.status_code}")

with open("voice.wav", "wb") as f:
    f.write(response.content)

# Validate downloaded file
size = os.path.getsize("voice.wav")
print(f"Downloaded file size: {size} bytes")
if size < 1000:
    raise Exception("Downloaded voice.wav is too small, probably invalid!")

print("ðŸŽžï¸ Creating video using FFmpeg...")

# Run ffmpeg
subprocess.run([
    "ffmpeg",
    "-y",
    "-loop", "1",
    "-i", BACKGROUND_IMAGE,
    "-i", "voice.wav",
    "-c:v", "libx264",
    "-tune", "stillimage",
    "-c:a", "aac",
    "-b:a", "192k",
    "-pix_fmt", "yuv420p",
    "-shortest",
    "-vf", "scale=1080:1920",
    "output.mp4"
], check=True)

print("âœ… Video created: output.mp4")

